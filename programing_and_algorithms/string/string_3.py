# Задача №3. Две еднакви букви
# Даден е низ. Известно е, че той съдържа точно две еднакви букви. Намерете тези букви.
# Вход
# От стандартния вход се въвежда един низ
# Изход
# Изведете буквата, която се среща два пъти.

def find_double_letter(str):
    for char in str:
        if str.count(char) == 2:
            return char

print(find_double_letter(input()))